// Day 6 - Objects in JavaScript

// creating an object
// objects store related data as key value pairs
const student = {
    name: "Kiran",
    age: 19,
    city: "Hyderabad",
    isStudent: true
}

console.log(student)


// accessing properties
// dot notation
console.log(student.name)    // Kiran
console.log(student.age)     // 19

// bracket notation
console.log(student["city"])   // Hyderabad
