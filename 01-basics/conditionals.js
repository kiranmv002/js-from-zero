// Day 4 - Conditionals in JavaScript

// if statement
// runs only if condition is true
let age = 18

if (age >= 18) {
    console.log("you can vote")
}


// if else
// runs one block or the other
let marks = 45

if (marks >= 50) {
    console.log("pass")
} else {
    console.log("fail")
}


// if else if else
// checks multiple conditions one by one
let score = 75

if (score >= 90) {
    console.log("grade: A")
} else if (score >= 75) {
    console.log("grade: B")
} else if (score >= 60) {
    console.log("grade: C")
} else {
    console.log("grade: F")
}
